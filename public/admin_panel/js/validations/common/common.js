$(document).on("click", ".change-status", function () {
    var id = $(this).data("id");
    var table = $(this).data("table");
    var flash = $(this).data("flash");
    var actionDiv = $(this);
    if (confirm("Do you really want to change status ?")){
        $.ajax({
            type: "post",
            headers: {
                "X-CSRF-TOKEN": $('meta[name="csrf-token"]').attr("content"),
            },
            data: { id: id, table: table, flash: flash },
            url: base_url + "/admin/change-status",
            beforeSend: function () {
                $(this).hide();
                actionDiv
                    .html(
                        "<i class='fa fa-spin fa-spinner' style='color: #0c0c0c !important;'></i>"
                    )
                    .show();
            },
            success: function (data) {
                console.log(data);
                if (data.status == 'true') {
                    var oTable = $("#arm_data_table").dataTable();
                    success_toast("Success", data.message);
                    if (data.user_status == "active") {
                        actionDiv.html(
                            "<i class='fa fa-toggle-on tgle-on' aria-hidden='true' title='Active'></i>"
                        );
                    } else {
                        actionDiv.html(
                            "<i class='fa fa-toggle-off tgle-off' aria-hidden='true' title='In-Active'></i>"
                        );
                    }
                    oTable.fnDraw(false);
                } else {
                    var oTable = $("#arm_data_table").dataTable();
                    fail_toast("Error", data.message);
                    oTable.fnDraw(false);
                }
            },
        });
    }
});

$(document).on("click", ".delete", function () {
    var id = $(this).data("id");
    var table = $(this).data("table");
    var flash = $(this).data("flash");
    var actionDiv = $(this);
    var base_url = $("#base_url").val();
    if (confirm("Do you really want to delete this record ?")) {
        $.ajax({
            type: "get",
            headers: {
                "X-CSRF-TOKEN": $('meta[name="csrf-token"]').attr("content"),
            },
            data: { id: id, table: table, flash: flash },
            url: base_url + "/admin/common-delete",
            beforeSend: function () {
                actionDiv
                    .html(
                        "<i class='fa fa-spin fa-spinner' style='color: #000000 !important;'></i>"
                    )
                    .show();
            },
            success: function (data) {
                var oTable = $("#arm_data_table").dataTable();
                oTable.fnDraw(false);
                success_toast("Success", data.message);
            },
            error: function (data) {
                console.log("Error:", data);
            },
        });
    }
});

function getStatesList(country_id) {
    var country_id = country_id;
    $.ajax({
        type: "post",
        headers: {
            "X-CSRF-TOKEN": $('meta[name="csrf-token"]').attr("content"),
        },
        data: { country_id: country_id },
        url: base_url + "/get-states-list",
        success: function (data) {
            if (data.status == "true") {
                $("#state_id").html(data.html);
            } else {
                $("#state_id").html('<option value="">Select State</option>');
            }
        },
    });
}

